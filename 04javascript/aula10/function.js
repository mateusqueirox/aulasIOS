function divisao(div1, div2) {
    return div1 / div2
}

function multiplicacao(mult1, mult2) {
    return mult1 * mult2
}

function soma(soma1, soma2) {
    return soma1 + soma2
}

function subtracao(sub1, sub2) {
    return sub1 - sub2
}

